    private void btTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btTimKiemActionPerformed
        // TODO add your handling code here:
        // Lấy mã sản phẩm người dùng gõ vào ô nhập liệu
        String maCanTim = txtMaSP.getText().trim();
        
        // Nếu người dùng không nhập gì mà bấm Tìm -> Tải lại toàn bộ bảng
        if (maCanTim.isEmpty()) {
            javax.swing.JOptionPane.showMessageDialog(this, "Vui lòng nhập Mã Sản Phẩm cần tìm vào ô Mã SP!");
            loadDataToTable(); 
            return;
        }
        
        // Lấy danh sách từ DAO để lọc
        java.util.ArrayList<model.Sach> danhSach = sachDAO.selectAll();
        boolean daThay = false;
        
        if (danhSach != null) {
            tableModel.setRowCount(0); // Xóa trắng bảng để chỉ hiện kết quả tìm kiếm
            
            for (model.Sach s : danhSach) {
                // So sánh mã tìm kiếm (không phân biệt chữ hoa/thường)
                if (s.getMasp().equalsIgnoreCase(maCanTim)) { 
                    
                    // 1. Nạp duy nhất cuốn sách tìm được lên bảng
                    tableModel.addRow(new Object[]{
                        s.getMasp(), 
                        s.getTensp(), 
                        s.getSoluong(), 
                        s.getDongia()
                    });
                    
                    // 2. Điền luôn thông tin lên các ô Text cho tiện thao tác (Sửa/Xóa)
                    txtTenSP.setText(s.getTensp());
                    txtSoLuong.setText(String.valueOf(s.getSoluong()));
                    txtDonGia.setText(String.valueOf(s.getDongia()));
                    
                    daThay = true;
                    break; // Tìm thấy rồi thì dừng vòng lặp luôn
                }
            }
        }
        
        // Nếu vòng lặp chạy xong mà không tìm thấy
        if (!daThay) {
            javax.swing.JOptionPane.showMessageDialog(this, "Không tìm thấy sản phẩm có mã: " + maCanTim);
            loadDataToTable(); // Hiển thị lại toàn bộ danh sách cũ
        }
    }//GEN-LAST:event_btTimKiemActionPerformed

