    //nút xoá
    private void btXoaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btXoaActionPerformed
        // TODO add your handling code here:
        int row = tblKhoHang.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Vui lòng chọn 1 dòng trên bảng để xóa!");
            return;
        }
        String maSP = tblKhoHang.getValueAt(row, 0).toString();
        int xacNhan = JOptionPane.showConfirmDialog(this, "Xóa sách mã " + maSP + "?", "Xác nhận", JOptionPane.YES_NO_OPTION);
        
        if (xacNhan == JOptionPane.YES_OPTION) {
            Sach sachXoa = new Sach(maSP, "", 0, 0.0, "", 0);
            quanLyKhoService.xoaSach(sachXoa);
            JOptionPane.showMessageDialog(this, "Xóa thành công!");
            loadDataToTable();
            // Xóa trắng ô nhập liệu sau khi xóa
            txtMaSP.setText(""); txtTenSP.setText(""); txtSoLuong.setText(""); txtDonGia.setText("");
        }
    }//GEN-LAST:event_btXoaActionPerformed

