    // Hàm nạp dữ liệu lên Bảng (Tự viết thêm vào đây)
    private void loadDataToTable() {
        tableModel.setRowCount(0); // Xóa trắng bảng cũ
        
        // Gọi thẳng DAO để lấy danh sách vì Service không hỗ trợ trả về List
        java.util.ArrayList<model.Sach> danhSach = sachDAO.selectAll();
        
        if (danhSach != null) {
            for (model.Sach s : danhSach) {
                // CHÚ Ý: Đổi tên các hàm getMasp(), getSoluong()... 
                // cho đúng với code trong file model.Sach.java của bạn nhé
                tableModel.addRow(new Object[]{
                    s.getMasp(), 
                    s.getTensp(), 
                    s.getSoluong(), 
                    s.getDongia() 
                });
            }
        }
    }
        
